public class CreateBufferedImageParameter6 {
	public CreateBufferedImageParameter parameterObject;
	public int minDrawW;
	public int minDrawH;
	public int maxDrawW;
	public int maxDrawH;

	public CreateBufferedImageParameter6(CreateBufferedImageParameter parameterObject, int minDrawW, int minDrawH,
			int maxDrawW, int maxDrawH) {
		this.parameterObject = parameterObject;
		this.minDrawW = minDrawW;
		this.minDrawH = minDrawH;
		this.maxDrawW = maxDrawW;
		this.maxDrawH = maxDrawH;
	}
}